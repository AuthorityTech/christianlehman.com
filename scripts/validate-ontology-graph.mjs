#!/usr/bin/env node
import fs from "fs";
import path from "path";
import vm from "node:vm";
import { fileURLToPath } from "url";
import { IDS } from "@editorialkit/schema";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const layout = fs.readFileSync(path.join(ROOT, "src", "app", "layout.tsx"), "utf8");
const blogIndex = fs.readFileSync(path.join(ROOT, "src", "app", "blog", "page.tsx"), "utf8");
const blogPost = fs.readFileSync(path.join(ROOT, "src", "app", "blog", "[slug]", "page.tsx"), "utf8");
let llms = fs.readFileSync(path.join(ROOT, "src", "app", "llms.txt", "route.ts"), "utf8");
const llmsManifest = path.join(ROOT, "src", "lib", "site-manifest.ts");
if (llms.includes("buildLlmsTxtBody") && fs.existsSync(llmsManifest)) {
  llms += `\n${fs.readFileSync(llmsManifest, "utf8")}`;
}
const robots = fs.readFileSync(path.join(ROOT, "src", "app", "robots.ts"), "utf8");

function fail(message) {
  console.error(`Ontology graph validation failed: ${message}`);
  process.exit(1);
}

function extractSchemaObject(source) {
  const match = source.match(/const schema\s*=\s*({[\s\S]*?});\n\nexport default function RootLayout/);
  if (!match) fail("Unable to find schema object in layout.tsx");
  return match[1];
}

// Constants from shared package — no regex extraction needed
const MACHINE_RELATIONS_TERM_ID = IDS.MR_TERM;
const MACHINE_RELATIONS_TERM_SET_ID = IDS.MR_TERM_SET;

const schema = vm.runInNewContext(
  `(${extractSchemaObject(layout)})`,
  {
    SITE_URL: "https://christianlehman.com",
    OG_IMAGE: "https://christianlehman.com/images/christian-lehman-cover-image.png",
    MACHINE_RELATIONS_TERM_ID,
    MACHINE_RELATIONS_TERM_SET_ID,
  },
  { timeout: 1000 }
);

const graph = schema?.["@graph"];
if (!Array.isArray(graph)) fail("Schema graph missing in layout.tsx");

const byId = new Map(
  graph
    .filter((node) => node && typeof node === "object" && typeof node["@id"] === "string")
    .map((node) => [node["@id"], node])
);

const personId = "https://christianlehman.com/#person";
const person = byId.get(personId);
const website = byId.get("https://christianlehman.com/#website");
if (!person) fail("Missing Christian canonical Person node");
if (!website) fail("Missing Christian website node");
if (person.worksFor?.["@id"] !== "https://authoritytech.io/#organization") {
  fail("Christian Person.worksFor must reference canonical AuthorityTech @id");
}

const knowsAbout = Array.isArray(person.knowsAbout) ? person.knowsAbout : [];
if (!knowsAbout.some((entry) => entry?.["@id"] === "https://machinerelations.ai/#term")) {
  fail("Christian Person.knowsAbout must reference canonical Machine Relations @id");
}

if (!String(person.description || "").toLowerCase().includes("architect")) {
  fail("Christian Person description must preserve architect framing");
}
if (website.creator?.["@id"] !== personId) {
  fail("WebSite.creator must reference canonical Christian person @id");
}
if (website.about?.["@id"] !== "https://machinerelations.ai/#term") {
  fail("WebSite.about must reference canonical Machine Relations @id");
}
if (/author:/.test(JSON.stringify(website))) {
  fail("WebSite must not use author; use creator");
}

if (!/"@type":\s*"WebPage"[\s\S]{0,220}?"@id":\s*`\$\{BASE\}\/blog`/s.test(blogIndex)) {
  fail("Blog index breadcrumb items must be WebPage objects");
}
// Blog post schema now generated by shared generateBlogJsonLd (checked in guard-ontology-schema.mjs)

// ─── Required node checks per doctrine ──────────────────────────────────────
const term = byId.get("https://machinerelations.ai/#term");
const termSet = byId.get("https://machinerelations.ai/glossary#definedtermset");
const org = byId.get("https://authoritytech.io/#organization");
const blog = byId.get("https://christianlehman.com/blog#blog");
const profilePage = byId.get("https://christianlehman.com/#profile-page");

if (!term) fail("Missing DefinedTerm node for Machine Relations");
if (!termSet) fail("Missing DefinedTermSet node for Machine Relations glossary");
if (!org) fail("Missing AuthorityTech Organization stub");
if (!blog) fail("Missing Blog node (christianlehman.com/blog#blog)");
if (blog["@type"] !== "Blog") fail("Blog node must be @type Blog");
if (blog.author?.["@id"] !== personId) {
  fail("Blog.author must reference canonical Christian person @id");
}
if (!profilePage) fail("Missing ProfilePage node (christianlehman.com/#profile-page)");
if (profilePage.mainEntity?.["@id"] !== personId) {
  fail("ProfilePage.mainEntity must reference canonical Christian person @id");
}
if (term.inDefinedTermSet?.["@id"] !== "https://machinerelations.ai/glossary#definedtermset") {
  fail("DefinedTerm.inDefinedTermSet must point to canonical term set @id");
}

// ─── Forbidden entity gates ─────────────────────────────────────────────────
// CL is a personal site. Jaxon Person, Service, SoftwareApplication are forbidden.
if (byId.has("https://jaxonparrott.com/#person")) {
  fail("Jaxon Person node is forbidden on CL — Jaxon's entity lives on jaxonparrott.com");
}
for (const node of graph) {
  if (node?.["@type"] === "Service") {
    fail("Service node is forbidden on CL — this is a personal site, not an agency");
  }
  if (node?.["@type"] === "SoftwareApplication") {
    fail("SoftwareApplication is forbidden on CL");
  }
}

const graphText = JSON.stringify(graph);
if (/Christian Lehman coined Machine Relations/i.test(graphText + "\n" + llms)) {
  fail("Christian surfaces must not imply he coined Machine Relations");
}
if (!/Machine Relations is the category Jaxon Parrott coined/i.test(llms)) {
  fail("llms.txt must explicitly state Jaxon coined Machine Relations");
}
if (!/brand-facing architect/i.test(graphText + "\n" + llms)) {
  fail("Christian surfaces must preserve brand-facing architect framing");
}

for (const bot of ["PerplexityBot", "ChatGPT-User", "anthropic-ai"]) {
  if (!robots.includes(bot)) fail(`robots must allow ${bot}`);
}

console.log("Ontology graph validation passed: Christian relationships intact.");
